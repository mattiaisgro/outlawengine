using namespace GL;

#ifndef LOADGL_MINIMAL
std::map<std::string, bool> GL::glextensions;
#endif

#ifdef WIN32

#define WIN32_LEAN_AND_MEAN 1
#include <windows.h>
#include <wingdi.h>

static HMODULE lib;

static int load_lib() {

	lib = LoadLibraryA("opengl32.dll");
	if(lib)
		return 0;

	return -1;
}

static void free_lib() {

	FreeLibrary(lib);
}

gl_func GL::glGetProcAddress(const char *name) {

	gl_func ptr = (gl_func) wglGetProcAddress(name);
	if (!ptr)
		ptr = (gl_func) GetProcAddress(lib, name);

	return (gl_func) ptr;
}

#elif defined(__linux__)

#include <dlfcn.h>
#include <GL/glx.h>
#include <string.h>

static void* lib;
static PFNGLXGETPROCADDRESSPROC glXGetProcAddress_;

static int load_lib() {
	lib = dlopen("libGL.so.1", RTLD_LAZY | RTLD_GLOBAL);
	if(!lib)
		return -1;

	glXGetProcAddress_ = (PFNGLXGETPROCADDRESSPROC) dlsym(lib, "glXGetProcAddressARB");
	if(!glXGetProcAddress_)
		return -1;

	return 0;
}

static void free_lib() {
	dlclose(lib);
}

gl_func GL::glGetProcAddress(const char *name) {

	gl_func ptr = (gl_func) glXGetProcAddress_((const GLubyte *) name);
	if (!ptr)
		ptr = (gl_func) dlsym(lib, name);
	return ptr;
}
#endif

bool GL::glIsSupported(const char* extension) {

#ifdef LOADGL_MINIMAL
	int num;
	glGetIntegerv(GL_NUM_EXTENSIONS, &num);
	for(int i = 0; i < num; i++)
		if(!strcmp((const char*) glGetStringi(GL_EXTENSIONS, i), extension))
			return true;

	return false;
#else
	return glextensions.count(std::string(extension));
#endif
}

void load_funcs();

int GL::loadgl() {

	if(load_lib()) return -1;

	load_funcs();

	free_lib();
	return 0;
}

int GL::loadglext() {

#ifndef LOADGL_MINIMAL

	if(!glGetIntegerv)
		return -1;

	int num;
	glGetIntegerv(GL_NUM_EXTENSIONS, &num);
	for(int i = 0; i < num; i++) {
		glextensions.insert(std::pair<std::string, bool>(
			std::string((const char*) glGetStringi(GL_EXTENSIONS, i)),
			true));
	}

#endif

	return 0;
}

